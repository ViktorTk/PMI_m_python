"""Задача 1: Анализ последовательности чисел
Напишите программу, которая:
1. Принимает на вход последовательность целых чисел, разделенных пробелами.
2. Находит и выводит:
    ○ Сумму всех четных чисел в последовательности.
    ○ Количество положительных чисел.
    ○ Максимальное нечетное число.
3. Определяет и выводит, является ли сумма всех чисел в последовательности
простым числом.
4. Если введенная последовательность содержит повторяющиеся элементы,
программа должна удалить все дубликаты и вывести новую последовательность. """



def is_prime(n):
    """Функция для проверки, является ли число простым"""
    if n <= 1:
        return False
    for i in range(2, int(n ** 0.5) + 1):
        if n % i == 0:
            return False
    return True

def analyze_sequence(sequence):
    """Функция для анализа последовательности чисел"""
    
    # Преобразуем строку в список целых чисел
    numbers = list(map(int, sequence.split()))
    
    # 1. Сумма всех четных чисел
    even_sum = sum(num for num in numbers if num % 2 == 0)
    
    # 2. Количество положительных чисел
    positive_count = len([num for num in numbers if num > 0])
    
    # 3. Максимальное нечетное число
    odd_numbers = [num for num in numbers if num % 2 != 0]
    max_odd = max(odd_numbers) if odd_numbers else None
    
    # 4. Определение простоты суммы всех чисел
    total_sum = sum(numbers)
    is_total_prime = is_prime(total_sum)
    
    # 5. Удаление дубликатов
    unique_numbers = list(set(numbers))
    
    # Результат
    print(f"Сумма всех четных чисел: {even_sum}")
    print(f"Количество положительных чисел: {positive_count}")
    print(f"Максимальное нечетное число: {max_odd if max_odd is not None else 'Отсутствует'}")
    print(f"Сумма всех чисел {'является' if is_total_prime else 'не является'} простым числом.")
    print(f"Новая последовательность без дубликатов: {unique_numbers}")

# Ввод последовательности чисел
sequence = input("Введите последовательность целых чисел, разделенных пробелами: ")
analyze_sequence(sequence)


